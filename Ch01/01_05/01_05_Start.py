# Minimum and Maximum
player_one_score = 10
player_two_score = 4
print(min(player_one_score, player_two_score))
print(min(0, 12, -19))

print(min('Kathryn', 'Katie'))
print(min('Angela', 'Bob'))  # Angela comes first in the alphabet

print(max(player_one_score, player_two_score))
player_three_score = 14
print(max(player_two_score, player_one_score, player_three_score))
print(max('Kathryn', 'Katie'))