# Python Rounding, Absolute Value, and Exponents

# round() 0.0 to 0.4 > under 0.5 to 0.9 > upper
my_gpa = 3.6
print(round(my_gpa))
amount_of_salt = 1.4
print(round(amount_of_salt))
apple = -1.2
print(round(apple))
google = -1.6
print(round(google))
poc = 1.5
print(round(poc))
# abs()
distance_away = -13
print(abs(distance_away))

# pow() base, exponent
print(pow(3, 2))
chance_of_tails = 0.5
in_a_row_tails = 3
print(pow(chance_of_tails, in_a_row_tails))

chance_of_one = 0.167
in_a_row_one = 2
print(pow(chance_of_one, in_a_row_one))